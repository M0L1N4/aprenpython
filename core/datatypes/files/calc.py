value1 = int(input('Enter first number: '))
value2 = int(input('Enter second number: '))

result = value1 + value2
print(value1, value2, sep='+', end='=')
print(result)

result = value1 - value2
print(value1, value2, sep='-', end='=')
print(result)

result = value1 * value2
print(value1, value2, sep='*', end='=')
print(result)

result = value1 / value2
print(value1, value2, sep='/', end='=')
print(result)
